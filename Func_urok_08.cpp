#include <iostream>
using namespace std;

void straight(int a, int b) {
    cout << " ";
    for (int i = 0; i < b; i++)
        cout << "-";
    cout << " ";
    cout << endl;
    for (int i = 0; i < a; i++) {
        cout << "|";
        for (int j = 0; j < b; j++)
            cout << " ";
        cout << "|";
        if (i < a) cout << endl;
    }
    cout << " ";
    for (int i = 0; i < b; i++)
        cout << "-";
    cout << endl;
}

int fact(int N) {
    if (N == 0)
        return 0;
    if (N == 1)
        return 1;
    return N * fact(N - 1);
}


int main()
{
    setlocale(LC_ALL, "ru");

    int N, K;
    cout << "Введите высоту и ширину прямоугольника: " << endl;
    cin >> N >> K;
    straight(N, K);

    int a;
    cin >> a;
    cout << fact(a);

    return 0;
}